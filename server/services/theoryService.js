function pickRandom(list, fallback) {
  if (!Array.isArray(list) || list.length === 0) {
    return fallback;
  }

  var index = Math.floor(Math.random() * list.length);
  return list[index];
}

function getTheoryForEmotion(emotionKey) {
  var base = THEORY_MAP[emotionKey] || THEORY_MAP.unknown;

  return {
    method: base.method,
    reply: pickRandom(base.replies, ''),
    suggestion: pickRandom(base.suggestions, ''),
    description: base.description
  };
}

